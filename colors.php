function get_colors ($colors) {
    global $themes ;
    return array (
        "primary" => explode ("!", $themes [$colors] [".mdl-color--primary"])[0],
        "primary-contrast" => explode ("!", $themes [$colors] [".mdl-color--primary-contrast"])[0],
        "primary-dark" => explode ("!", $themes [$colors] [".mdl-color--primary-dark"])[0],
        "secondary" => explode ("!", $themes [$colors] [".mdl-color--secondary"])[0],
        "info" => explode ("!", $themes [$colors] [".mdl-color--accent"])[0]);
}

function get_color_list () {
    global $themes ;
    $list = array () ;
    foreach ($themes as $t => $v) {
        // array_push ($list, $t);
        // array_push ($list, ucwords (str_replace ("_", " ", $t)));
        $list [$t] = ucwords (str_replace ("_", " ", $t)) ;
    }
    return $list;
}

